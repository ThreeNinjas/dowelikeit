<h2>A Poem For Alex</h2>

<p>by Jason J Brunet</p>


<h4>The Freeverse Part</h4>
<p>
A woman scented candle<br>
It smells like smiles<br>
like touching<br>
like laughter and water<br>
It burns for 60 hours but I would want it for longer<br><br>

A man of sincerity catches the scent and it stays with him<br>
She smells like smiling<br><br>

Would that the smiling may lead to touching<br>
</p>

<h4>The Haiku Part</h4>
<p>
I swiped right on her<br>
Her wit made me want more wit<br>
So glad she's not spam<br>
</p>

<h4>The Part That Rhymes</h4>
<p>
Roast beef candles are for chumps<br>
Alex has such lovely bumps<br>
When I was younger I had the mumps<br>
You're so funny and smart and pretty.
</p>
